CREATE TABLE PROFESSOR(
	ID INTEGER PRIMARY KEY AUTOINCREMENT,
	NOME VARCHAR(10),
	USUARIO VARCHAR(10),
	SENHA VARCHAR(50),
	UNIQUE(USUARIO)
);

CREATE TABLE QUESTAO(
	ID INTEGER PRIMARY KEY AUTOINCREMENT,
	TIPO INTEGER,
	MODELO VARCHAR(30),
	DIFICULDADE INTEGER,
	RANGE_MIN INTEGER,
  RANGE_MAX INTEGER,
	UNIQUE(MODELO)
);

CREATE TABLE PROVA(
	ID INTEGER PRIMARY KEY AUTOINCREMENT,
	PROFESSOR INTEGER,
	TIPO INTEGER,
	DIFICULDADE INTEGER,
	QTD_QUESTOES INTEGER,
	FOREIGN KEY(PROFESSOR) REFERENCES PROFESSOR(ID)
);

CREATE TABLE RESOLUCOES(
	ID INTEGER PRIMARY KEY AUTOINCREMENT,
	QTD_ACERTOS INTEGER,
	EMAIL VARCHAR(80),
	NOME_ALUNO VARCHAR(20),
	PROVA INTEGER,
	UNIQUE(EMAIL,PROVA),
	FOREIGN KEY(PROVA) REFERENCES PROVA(ID)
);
